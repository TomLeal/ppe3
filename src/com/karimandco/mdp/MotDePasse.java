    public Boolean verifMDP(){
        Boolean result = false;
        int tailleMDP = recupMDP().length();
        if (tailleMDP<6 && tailleMDP>0) {
            jLabelEtat.setForeground(Color.red);
            jLabelEtat.setText("Format non ok");
        }else if(tailleMDP>=6){
            jLabelEtat.setForeground(Color.BLUE);
            jLabelEtat.setText("Format ok");
            result = true;
        }else{
            jLabelEtat.setText(" ");
        }
        return result;
    }

