    /**
     * Remplie certains boutons du tableau avec des chiffres de 0 à 9.
     */
    public void initialiseTableau(){
        JButton bouton = null;
        int nbAlea;
        for (int i = 0; i < 10; i++) {
            nbAlea=rand.nextInt(24);
            bouton = tabBouton.get(nbAlea);
            while(!bouton.getText().equals("")){
                nbAlea=rand.nextInt(24);
                bouton = tabBouton.get(nbAlea);
            }
            tabBouton.get(nbAlea).setText(String.valueOf(i));
        }
        
    }

