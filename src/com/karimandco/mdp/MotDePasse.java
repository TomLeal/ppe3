    /**
     * Quand on clique dur le bouton "Retour", cela éfface le dernier chiffre du jLabel.
     * @param evt 
     */
    private void jButtonRetourMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonRetourMouseClicked
        String mdp = recupMDP();
        if (!mdp.equals("")) {
            String result = "";
            char[] chiffreTab = mdp.toCharArray();
            for (int i = 0; i < chiffreTab.length - 1; i++) {
                result = result + chiffreTab[i];
            }
            jLabelResultat.setText(result);
            verifMDP();
        }
    }//GEN-LAST:event_jButtonRetourMouseClicked


